const terminal = document.getElementById("terminal");
const downloadBtn = document.getElementById("downloadBtn");
const langToggleBtn = document.getElementById("langToggleBtn");

let currentLang = "EN";
let blockIndex = 0;
let charIndex = 0;

function typeText(text, callback) {
    if (charIndex < text.length) {
        const char = text[charIndex];
        terminal.innerHTML += char === "\n" ? "<br>" : char;
        charIndex++;
        setTimeout(() => typeText(text, callback), 1);
    } else {
        callback();
    }
}

function startTypingBlocks(textBlocks) {
    if (blockIndex < textBlocks.length) {
        charIndex = 0;
        typeText(textBlocks[blockIndex], () => {
            terminal.innerHTML += "<br><br>";
            blockIndex++;
            setTimeout(() => startTypingBlocks(textBlocks), 400);
        });
    } else {
        terminal.innerHTML += '<span class="cursor"></span>';
        setTimeout(() => {
            document.getElementById("socialLinks").classList.remove("hidden");
            document.getElementById("projectLinks").classList.remove("hidden");

            document.getElementById("socialLinks").classList.add("visible");
            document.getElementById("projectLinks").classList.add("visible");
        }, 500);
    }
}

function startTyping() {
    blockIndex = 0;
    charIndex = 0;
    terminal.innerHTML = "";
    document.getElementById("socialLinks").classList.add("hidden");
    document.getElementById("projectLinks").classList.add("hidden");

    if (currentLang === "EN") {
        startTypingBlocks(textBlocksEN);
    } else {
        startTypingBlocks(textBlocksES);
    }
}

downloadBtn.addEventListener("click", () => {
    const pdfContent = (currentLang === "EN" ? textBlocksEN : textBlocksES).join("\n\n");
    const blob = new Blob([pdfContent], { type: "text/plain" });
    const link = document.createElement("a");
    link.href = URL.createObjectURL(blob);
    link.download = currentLang === "EN" ? "Martin_Daniels_Resume_EN.txt" : "Martin_Daniels_Resume_ES.txt";
    link.click();
    URL.revokeObjectURL(link.href);
});

langToggleBtn.addEventListener("click", () => {
    if (currentLang === "EN") {
        currentLang = "ES";
        langToggleBtn.textContent = "English";
    } else {
        currentLang = "EN";
        langToggleBtn.textContent = "Español";
    }
    startTyping();
});


startTyping();




